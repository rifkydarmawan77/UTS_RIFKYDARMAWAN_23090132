input_bilangan_pertama = int(input("masukan bilangan pertama = "))
input_bilangan_kedua = int(input("masukan bilangan kedua = "))

penjumlahan = input_bilangan_pertama + input_bilangan_kedua
pengurangan = input_bilangan_pertama - input_bilangan_kedua
modulus = input_bilangan_pertama % input_bilangan_kedua

print("hasil penjumlahan",penjumlahan)
print("hasil pengurangan",pengurangan)
print("hasil modulus", modulus)